/** 신규 견적문의 폼 스키마 */
export const inquiryFormSchema = z.object({
  companyName: z.string().min(1, "사업장명을 입력해주세요"),
  department: z.string().optional().or(z.literal("")),
  contactName: z.string().min(1, "이름을 입력해주세요"),
  notificationMethod: z.literal("email").default("email"),
  email: z
    .string()
    .min(1, "이메일을 입력해주세요")
    .email("올바른 이메일 형식이 아닙니다"),
  phone: z
    .string()
    .regex(
      /^\d{2,3}-\d{3,4}-\d{4}$/,
      "올바른 전화번호 형식이 아닙니다 (예: 010-1234-5678)"
    ),
  address: z.string().optional().or(z.literal("")),
  addressDetail: z.string().optional().or(z.literal("")),
  wasteTypes: z
    .array(z.string())
    .min(1, "폐기물 종류를 최소 하나 이상 선택해주세요"),
  message: z.string().optional().or(z.literal("")),
  photoUrls: z.array(z.string()).optional().default([]),
  agreement: z.literal(true, {
    errorMap: () => ({ message: "개인정보 수집·이용에 동의하셔야 합니다" }),
  }),
  marketingConsent: z.boolean().optional().default(false),
});

export type InquiryFormValues = z.infer<typeof inquiryFormSchema>;

